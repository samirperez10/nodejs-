
function suma(num1, nume2){
    const result = num1 + nume2;
    return result;
}
function resta(num1, nume2){
    const result = num1 - nume2;
    return result;
}
function mult(num1, nume2){
    const result = num1 * nume2;
    return result;
}
function div(num1, nume2){
    const result = num1 / nume2;
    return result;
}
module.exports = {suma, resta, mult, div};